// Rileva lingua del browser
export const rilevaLingua = () => {
  const linguaBrowser = navigator.language.split('-')[0]
  return lingue[linguaBrowser] ? linguaBrowser : 'en'
}

// Hook per l'uso delle traduzioni
import { useState, useEffect } from 'react'

export const useTraduzioni = () => {
  const [lingua, setLingua] = useState(() => {
    // Prova a recuperare la lingua salvata, altrimenti rileva
    return localStorage.getItem('appLingua') || rilevaLingua()
  })

  const traduci = (chiave) => {
    return lingue[lingua]?.[chiave] || lingue.en[chiave] || chiave
  }

  const cambiaLingua = (nuovaLingua) => {
    setLingua(nuovaLingua)
    localStorage.setItem('appLingua', nuovaLingua)
  }

  useEffect(() => {
    // Salva la lingua quando cambia
    localStorage.setItem('appLingua', lingua)
  }, [lingua])

  return {
    t: traduci,
    lingua,
    cambiaLingua,
    lingueDisponibili: Object.keys(lingue)
  }
}